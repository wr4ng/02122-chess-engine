					if (openings[i].Length <= moves.Count || openings[i][j] != moves[j])
					{
						break;
					}
					if (j == moves.Count - 1 && openings[i].Length > moves.Count)
					{
						nextMoves.Add(openings[i][j + 1]);
						break;
					}
				}
			}
			if (nextMoves.Count > 0)
			{
				Random random = new();
				int index = random.Next(0, nextMoves.Count - 1);
				return (nextMoves[index], true);
			}
